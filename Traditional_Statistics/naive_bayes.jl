"""
function fit_distribution(X)
    len = size(X,2)
    dist=Vector{Normal{Float64}}(undef, length(1:len))
    for i=1:len

        dist[i]=Normal(mean(X[:,i]),std(X[:,i]))
    end

    return dist
end

